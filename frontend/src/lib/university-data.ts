/** 获取所有大学名称列表 */
export function getAllUniversityNames(): string[] {
  return UNIVERSITIES_985.map((u) => u.name);
}

/** 根据大学名称获取其学院列表 */
export function getDepartmentsByUniversity(universityName: string): string[] {
  const uni = UNIVERSITIES_985.find((u) => u.name === universityName);
  return uni?.departments ?? [];
}

/** 搜索大学（模糊匹配名称或简称） */
export function searchUniversities(keyword: string): UniversityData[] {
  if (!keyword) return UNIVERSITIES_985;
  const kw = keyword.toLowerCase();
  return UNIVERSITIES_985.filter(
    (u) =>
      u.name.toLowerCase().includes(kw) ||
      u.short_name.toLowerCase().includes(kw) ||
      u.province.includes(kw)
  );
}

